public class SeparatorsOperators {
    public static void main(String[] args) {
        
        String operators = "+    -    *    /    %    ^    &    |    !    ~\n" +
                " =    +=   -=   *=   /=   %=    ^=    ++    --\n" +
                " ==   !=    <   >    &=  >>=   <<=   >=   <= \n" +
                " ||   &&    >>    <<    ?:";

        System.out.println(operators);

        System.out.println("____________________________");

        String separators = "[ ]   ( )   { }  " ;

        System.out.println(separators);
    }
}
