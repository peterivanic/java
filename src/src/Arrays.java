public class Arrays {

    public static void main(String[] args) {

        int[] numbers = {1,2,3,4,5};

        System.out.println(numbers[0]);

        numbers[0] = 5;

        System.out.println(numbers[0]);



        String[] words = {"one","two","three","four"};

        System.out.println(words[0]);

        words[0] = "five";

        System.out.println(words[0]);
        
    }
}
